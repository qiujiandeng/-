#练习：
#    指定一个圆的半径为　ｒ＝３厘米
#   　１）计算此圆的周长是多少？
#  　２）计算此圆的面积是多少？
#   　圆周率：3.1415926
#   　周长　＝　圆周率　×　半径　×　２
#   　面积　＝　圆周率　×　半径　×　半径

r = 3
pi = 3.1415926

length = pi * r * 2          # a = 3.1415926 * 3 * 2
print("周长是：",length,"厘米")

area = pi * r * r   #r**2　　　＃ｂ = 3.1415926 * 3 * 3
print("面积式：",area,"平方厘米")